import socket
import random

def get_host_ip():
    """ดึง IP Address ของเครื่องตัวเองในวง LAN เพื่อให้ Client ตัวอื่นกรอกได้"""
    s = socket.socket(socket.AF_INET, socket.SOCK_DGRAM)
    try:
        # ลองเชื่อมต่อออกข้างนอกเพื่อดูว่าใช้ Interface ไหน (ไม่ต้องต่อติดจริง)
        s.connect(('8.8.8.8', 80))
        ip = s.getsockname()[0]
    except Exception:
        ip = '127.0.0.1'
    finally:
        s.close()
    return ip

